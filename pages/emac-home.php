        <p>EMAC serves as a repository and integration platform for modeling and analysis tools focused on the study of exoplanetary atmospheres. EMAC is continually being updated with new software applications; if you have suggestions for improvements or new applications, please email us at gsfc.emac at gmail.com.</p>
        <p>EMAC is a collaboration between the GSFC Sellers Exoplanet Environments Collaboration (SEEC) and the GSFC Community Coordinated Modeling Center (CCMC). The P.I. is Avi M. Mandell, and the Deputy P.I. is Antti Pulkinnen; more information on EMAC staffing and organization will be posted shortly.</p>
